public class Vehicle {
    private String name;
    private int amtOfWheels;
    private String speed;

    public Vehicle() {
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAmtOfWheels() {
        return amtOfWheels;
    }

    public void setAmtOfWheels(int amtOfWheels) {
        this.amtOfWheels = amtOfWheels;
    }

    public String getSpeed() {
        return speed;
    }

    public void setSpeed(String speed) {
        this.speed = speed;
    }
}
